package c2_operators;

public class E09_MinMax {
    public static void main(String[] args) {
        double maxd = Double.MAX_VALUE;
        double mind = Double.MIN_VALUE;
        System.out.println("Max value of Double is: " + maxd);
        System.out.println("Min value of Double is: " + mind);

        float maxf = Float.MAX_VALUE;
        float minf = Float.MIN_VALUE;
        System.out.println("Max value of Float is: " + maxf);
        System.out.println("Max value of Float is: " + minf);
    }
}
